class concatination
{
	public static void main(String[] args) 
	{

		System.out.println(10+20); //30
		System.out.println('c'+10); //109
		System.out.println("100"+20); //10020
		System.out.println("sheela"+10); //sheela10
		System.out.println(10+"laila"+(400+20)); //10laila420
		System.out.println(10+20+"hello"+40); //30hello40
		System.out.println("ram"+'a'); //rama
		System.out.println("kamla"+true); //kamlatrue
		System.out.println("hello"+"world"); //helloworld
	}
}
